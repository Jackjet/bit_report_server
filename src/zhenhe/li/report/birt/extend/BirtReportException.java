package zhenhe.li.report.birt.extend;

public class BirtReportException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -26486694476852950L;

	public BirtReportException() {
	}

	public BirtReportException(String message, Throwable cause) {
		super(message, cause);
	}

	public BirtReportException(String message) {
		super(message);
	}

	public BirtReportException(Throwable cause) {
		super(cause);
	}
	
}
